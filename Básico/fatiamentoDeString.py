"""
A função len retorna a qtd de caracteres da str
"""

variavel = 'Olá mundo'

print(variavel[4:])
print(variavel[4:7])
print(variavel[1:5])
print(variavel[:5])
print(variavel[-8:-2])
print(len(variavel))
print(variavel[0:len(variavel):1])
print(variavel[0:9:2])
print(variavel[::-1])

